my_lst=[10,20,30,40]
my_lst.append(50)
print(my_lst)

#extend function
my_lst1=[10,20,30,40]
my_lst1.extend([60,70])
print(my_lst1)

#insert function
my_lst2=[10,20,30,40]
my_lst2.insert(2,25)
print(my_lst2)

#pop function
my_lst3=[10,20,30,40]
my_lst3.pop()
print(my_lst3)

#clear function
my_lst4=[10,20,30,40,50]
my_lst4.clear()
print(my_lst4)

#sort function
mylst5=[14,32,30,44,75]
